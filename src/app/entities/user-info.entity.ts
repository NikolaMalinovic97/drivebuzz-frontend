export class UserInfo {

    firstName: string;
    lastName: string;
    email: string;
    phone: string;

    constructor() {}

    getFirstName() {
        return this.firstName;
    }

    getLastName() {
        return this.lastName;
    }

    getEmail() {
        return this.email;
    }

    getPhone() {
        return this.phone;
    }
}
